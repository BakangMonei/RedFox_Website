            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet ShowAction</title>");
            out.println("</head>");
            out.println("<body>");
            String starttime = request.getParameter("starttime");
            String endtime = request.getParameter("endtime");
            String price = (String)request.getParameter("price");
